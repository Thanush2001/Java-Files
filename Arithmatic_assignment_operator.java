public class Arithmatic_assignment_operator
{
    public static void main(String[] args)
    {
        int a=123;
        System.out.println("a="+a);
        a=a+10;// a+=10
        System.out.println("a+10="+a);
        a-=10;
        System.out.println("a-10="+a);
        a*=10;
        System.out.println("a*10="+a);
        a/=10;
        System.out.println("a/10="+a);
        a%=10;
        System.out.println("a%10="+a);
    }
}
