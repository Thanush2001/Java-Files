/*
Unary operator in java
1. pre increment
2. post increment
 */
public class Unary_operator
{
    public static void main(String[] args)
    {
        int a=10;
        System.out.println(a);
        a++;//a=a+1
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
    }
}
